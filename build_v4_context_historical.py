def validate_and_compare(final_v4, final_old_path):
    """Compare v4 output with current production FINAL."""
    old = pd.read_csv(final_old_path)
    old['Fecha'] = old['Fecha'].astype(str)
    final_v4['Fecha'] = final_v4['Fecha'].astype(str)
    
    merged = old.merge(final_v4, on='Fecha', suffixes=('_old', '_v4'))
    
    print("\n" + "="*60)
    print("VALIDATION: v4 vs production")
    print("="*60)
    
    # Gate changes
    gate_changed = merged[merged['gate_final_old'] != merged['gate_final_v4']]
    print(f"\nGate changes: {len(gate_changed)} / {len(merged)} days")
    for _, r in gate_changed.iterrows():
        print(f"  {r['Fecha']}: {r['gate_final_old']} → {r['gate_final_v4']}"
              f"  veto={r['veto_agudo']}"
              f"  reason={r['reason_text'][:80]}")
    
    # Veto agudo stats
    veto_days = final_v4[final_v4['veto_agudo'] == True]
    print(f"\nVeto agudo activated: {len(veto_days)} days")
    for _, r in veto_days.iterrows():
        print(f"  {r['Fecha']}: ln_pre_veto={r['ln_pre_veto']:.3f} → ln_used={r['lnRMSSD_used']:.3f}"
              f"  gate={r['gate_final']}")
    
    # Reason_text coverage
    has_reason = final_v4['reason_text'].str.len() > 0
    print(f"\nDays with reason_text: {has_reason.sum()} / {len(final_v4)}")
    
    # Gate distribution comparison
    print(f"\nGate distribution:")
    for g in [VERDE, AMBAR, ROJO, NO]:
        n_old = (merged['gate_final_old'] == g).sum()
        n_v4 = (merged['gate_final_v4'] == g).sum()
        print(f"  {g}: {n_old} → {n_v4} (delta {n_v4-n_old:+d})")
    
    return gate_changed


