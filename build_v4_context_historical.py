def build_context(rev: pd.DataFrame, acts: pd.DataFrame) -> pd.DataFrame:
    """
    Build ENDURANCE_HRV_sleep.csv from historical exports.

    v4: sleep.csv = Polar sleep/nightly ONLY.
    Training load is in sessions_day.csv (generated by build_sessions.py).
    """
    
    # --- Polar sleep: from reviewer merge ---
    sleep_cols = [
        'Fecha',
        'sleep_score', 'sleep_continuity', 'sleep_efficiency',
        'sleep_rem', 'sleep_n3', 'sleep_long_interruptions',
        'sleep_span', 'asleep_duration',
        'nightly_rmssd', 'nightly_rri', 'nightly_resp_int',
    ]
    rev_sleep = rev[[c for c in sleep_cols if c in rev.columns]].copy()
    
    # Parse durations to minutes
    def iso_dur_to_min(val):
        if pd.isna(val) or val == '':
            return np.nan
        s = str(val)
        if s.startswith('PT'):
            s = s[2:]
            hours = 0
            mins = 0
            if 'H' in s:
                parts = s.split('H')
                hours = float(parts[0])
                s = parts[1]
            if 'M' in s:
                parts = s.split('M')
                mins = float(parts[0])
                s = parts[1] if len(parts) > 1 else ''
            secs = 0
            if 'S' in s:
                secs = float(s.replace('S', ''))
            return hours * 60 + mins + secs / 60
        return np.nan
    
    if 'asleep_duration' in rev_sleep.columns:
        rev_sleep['polar_sleep_duration_min'] = rev_sleep['asleep_duration'].apply(iso_dur_to_min)
    if 'sleep_span' in rev_sleep.columns:
        rev_sleep['polar_sleep_span_min'] = rev_sleep['sleep_span'].apply(iso_dur_to_min)
    
    # Rename to context schema
    rename_map = {
        'sleep_score': 'polar_sleep_score',
        'sleep_continuity': 'polar_continuity',
        'sleep_efficiency': 'polar_efficiency_pct',
        'sleep_rem': 'polar_rem_pct',
        'sleep_n3': 'polar_deep_pct',
        'sleep_long_interruptions': 'polar_interruptions_long',
        'nightly_rmssd': 'polar_night_rmssd',
        'nightly_rri': 'polar_night_rri',
        'nightly_resp_int': 'polar_night_resp',
    }
    rev_sleep = rev_sleep.rename(columns={k: v for k, v in rename_map.items() if k in rev_sleep.columns})
    
    # Efficiency from raw data if available
    if 'efficiency_pct' in rev.columns:
        rev_sleep['polar_efficiency_pct'] = rev['efficiency_pct']
    if 'continuity_index' in rev.columns:
        rev_sleep['polar_continuity_index'] = rev['continuity_index']
    
    context_cols = [
        'Fecha',
        'polar_sleep_duration_min', 'polar_sleep_span_min',
        'polar_deep_pct', 'polar_rem_pct',
        'polar_efficiency_pct', 'polar_continuity', 'polar_continuity_index',
        'polar_interruptions_long', 'polar_sleep_score',
        'polar_night_rmssd', 'polar_night_rri', 'polar_night_resp',
    ]
    context = rev_sleep[[c for c in context_cols if c in rev_sleep.columns]].copy()
    context = context.sort_values('Fecha').reset_index(drop=True)
    
    # --- Sleep percentiles ---
    dur = context['polar_sleep_duration_min'].dropna()
    context['sleep_dur_p10'] = float(dur.quantile(0.10)) if len(dur) >= 20 else np.nan
    context['sleep_dur_p90'] = float(dur.quantile(0.90)) if len(dur) >= 20 else np.nan
    
    intl = context.get('polar_interruptions_long', pd.Series(dtype=float)).dropna()
    context['sleep_int_p90'] = float(intl.quantile(0.90)) if len(intl) >= 20 else np.nan
    
    return context


